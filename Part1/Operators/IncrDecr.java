class IncrDecr{
public static void main(String args[]){
 //prefix incr
  int x=10;
  int y=++x;
System.out.println(x);
System.out.println(y);
//postfix incr
  int a=10;
  int b=x++;
System.out.println(a);
System.out.println(b);
 //prefix decr
  int c=10;
  int d=--x;
System.out.println(c);
System.out.println(d);
//postfix decr
  int p=10;
  int q=x--;
System.out.println(p);
System.out.println(q);

}
}